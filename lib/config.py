"""冲稳保算法阈值与配额（DESIGN §5）。全部可调，集中放这里，便于分省微调。

注意：这些是"信息整理"用的分档边界，不是录取概率。绝不输出"概率 X%"。
"""

# ── 冲稳保分档（比值法，差值比 = (r_avg - rank) / rank）─────────────
# 差值比为正 = 考生位次数字小于院校均值 = 考生更优 = 越靠"保"
RATIO_BAO = 0.10          # 差值比 >= +0.10            → 保
RATIO_WEN_LOW = -0.08     # -0.08 <= 差值比 < +0.10    → 稳
RATIO_CHONG_LOW = -0.20   # -0.20 <= 差值比 < -0.08    → 冲
#                          差值比 < -0.20             → 不列（差太多）

# ── 头部考生兜底（位次很小时比值法过紧，改用绝对位次差）─────────────
SMALL_RANK_THRESHOLD = 1000   # rank < 此值时启用绝对差分档
ABS_BAO = 150                 # (r_avg - rank) >= 150     → 保
ABS_WEN_LOW = -80             # -80 <= 差 < 150           → 稳
ABS_CHONG_LOW = -300          # -300 <= 差 < -80          → 冲
#                               差 < -300               → 不列

# ── 招生计划大小年方向性修正（只提示，不改分档数字）──────────────────
PLAN_CHANGE_THRESHOLD = 0.15  # 当年 vs 去年计划数变动超 ±15% → 给提示

# ── 梯度配额建议（冲/稳/保 占该省可填志愿数的比例）──────────────────
QUOTA = {"冲": 0.20, "稳": 0.50, "保": 0.30}

# ── 跨年数据使用策略（实测修正）─────────────────────────────────
# 广东 2023–2025 实测：按"投档单位id(组号)"跨年对齐，三年仅 12% 能匹配（组号年年重组）；
# 按"院校代码"对齐则 89% 稳定。结论：
#   · 专业组【分档基准取最新可得年位次】，不做跨年均值（组号重组→均值会张冠李戴）；
#   · 近 3 年数据用在【院校层面】算大小年/冷热趋势（89% 稳定）作校准提示；
#   · 区间 r_band 仍如实反映该 id 自身覆盖的年份（对约 12% 真稳定的组才有多年波动）。
# 不取 5 年：跨改革年不可比、时效差、组号越久越失真。
RECENT_YEARS = 3
# 院校近3年位次中位数变动超此阈值 → 给冷热方向提示（位次变小=升温/更难，变大=降温/更易）
SCHOOL_TREND_THRESHOLD = 0.10
# 跨年同组号稳定性闸门：某 id 往年位次相对最新年偏离超此比例 → 判定"组号重组、实为不同组"，
# 不纳入多年区间/招生计划YoY（避免张冠李戴）。仅偏离在阈内的年份才算"同组多年"。
STABILITY_RATIO = 0.5
