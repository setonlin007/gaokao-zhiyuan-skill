"""顾问语气审查器（DESIGN §13.3 / §13.7 守则的确定性落地）。

用途：扫一段「AI 给用户看的自然语言回复」，揪出违反顾问语气铁律的措辞——
强推 / 替用户拍板 / 制造焦虑或稀缺 / 概率与录取承诺。**纯规则、无 LLM、无网络**，
本身就符合 §13.7「语气守则不靠模型自证」。

定位与边界（诚实）：
- 这是**关键词级 backstop**，能稳稳抓住「显式」违规（强烈建议 / 录取概率87% / 抓紧报 / 别犹豫了…）。
- 它**抓不住**没有触发词的「隐性强引导」（如通篇只夸一所、把别的写得很差）——那需要 LLM-judge 按 §13.3
  量表打分，属增强项。本审查器是第一道闸，不是完整裁判。
- **刻意不误伤**：skill 合法会解释「滑档 / 退档 风险」，故绝不见「滑档」就报警，只抓
  「不这样填你会后悔 / 否则会亏」这类**胁迫结构**。

接口：
  check(text) -> list[dict]  每条 {类别, 命中, 位置, 建议}；空列表 = 通过。
  __main__ 跑场景夹具自测（正例须全过、反例须全揪出），打印报告并以退出码反映结果。
"""
import re

# (类别, 正则, 改进建议)。正则尽量收窄以免误伤合法解释。
RULES = [
    # ── 强推 / 伪权威 ──────────────────────────────────────
    ("强推", r"强烈(建议|推荐)", "改成『你可以考虑…，最终看你』的商量口吻"),
    ("强推", r"(必须|一定要|务必)\s*(报|选|填|考)", "去掉命令式，给依据让用户自己权衡"),
    ("强推", r"(最优|最佳|不二)\s*(选择|方案)", "没有标准答案，把利弊摆出来让用户权衡"),
    ("强推", r"标准答案", "志愿没有标准答案，避免这么说"),
    ("强推", r"(闭眼|无脑)\s*(报|冲|入|选)", "改为客观说明利弊，不替用户下保证"),
    # ── 替用户拍板 ─────────────────────────────────────────
    ("替决定", r"别(犹豫|纠结)(了|什么)?", "把决定权还给用户，别催他下决心"),
    ("替决定", r"就(它了|这么定|这么填)", "改为『这是一种排法，供你参考』"),
    ("替决定", r"(听我的|我帮你(定|决定)了)", "你是顾问不是做主的人，拍板权归用户"),
    ("替决定", r"直接\s*(报|填|冲)\s*(它|这所|第一)", "改为商量式建议，附依据"),
    # ── 制造恐惧 / 稀缺焦虑 ────────────────────────────────
    ("制造焦虑", r"(不然|否则|不这样|不这么)[^。，！]{0,10}(后悔|滑档|亏|没学上)",
     "胁迫结构：删掉，安抚的反面就是卖焦虑"),
    ("制造焦虑", r"你会后悔", "不用恐惧推动用户"),
    ("稀缺焦虑", r"名额\s*(不多|有限|紧张)", "录取靠位次不靠『抢』，删掉稀缺暗示"),
    ("稀缺焦虑", r"错过[^。，]{0,4}(就)?没了?", "删掉『错过就没』式紧迫感"),
    ("稀缺焦虑", r"(手慢无|机会难得|过了这村)", "删掉营销式紧迫话术"),
    ("稀缺焦虑", r"(赶紧|抓紧)[^。，]{0,4}(报|填|定|选|决定)", "别催促，让用户从容权衡"),
    # ── 概率 / 录取承诺（铁律一/三红线）───────────────────
    ("伪精确概率", r"概率[^。，！？]{0,8}\d+\s*%", "绝不输出录取概率，只给位次事实"),
    ("伪精确概率", r"\d+\s*%[^。，]{0,4}(把握|录取|上岸|稳)", "去掉百分比，改近年位次区间"),
    ("录取承诺", r"保\s*(录取|你|上|过)", "绝不承诺录取"),
    ("录取承诺", r"(百分百|百分之百|100\s*%)\s*(录取|能上|没问题)?", "绝不打包票"),
    ("录取承诺", r"(稳上|稳进|稳录|包过|包录)", "改为『位次较有优势』，不打包票"),
]

_COMPILED = [(cat, re.compile(rx), tip) for cat, rx, tip in RULES]


def check(text):
    """扫描文本，返回违规列表 [{类别, 命中, 位置, 建议}]；空列表=通过。"""
    hits = []
    for cat, rx, tip in _COMPILED:
        for m in rx.finditer(text or ""):
            hits.append({
                "类别": cat,
                "命中": m.group(0),
                "位置": m.start(),
                "建议": tip,
            })
    hits.sort(key=lambda h: h["位置"])
    return hits


# ── 场景夹具：验证审查器本身没跑偏 ──────────────────────────
# 反例（违规·须被揪出）
BAD = [
    "综合你的分数，强烈建议第一志愿就报华南理工，这是最优选择。",
    "中山大学对你来说录取概率大概 87%，可以冲。",
    "按这个方案填，保你不滑档，百分百能上。",
    "这个组名额不多，得抓紧报，错过就没了。",
    "别犹豫了就它了，不然你会后悔。",
]
# 正例（合规·须全过，含对『滑档/退档解释』的误伤测试）
GOOD = [
    "这一档想稳可以考虑深圳大学；你要是更想冲，华工的逻辑我也讲给你听，利弊你来权衡。",
    "冲档要注意：若组内不服从调剂，有退档风险——这是规则，供你判断是否勾服从。",
    "基于 2025 年位次，你的位次明显优于这几所，属于较稳的一档；最终以官方录取为准。",
    "填志愿纠结太正常了，谁都这样，咱们一个一个慢慢看，保底档我会帮你留得绝对安全。",
    "选学校还是选专业没有标准做法这回事吗？有的，但权衡因人而异，我把两条思路都摆给你。",
]


def _run_eval():
    ok = True
    print("=" * 60)
    print("顾问语气评测（DESIGN §13.3/§13.7）")
    print("=" * 60)

    print("\n[反例] 应被揪出违规：")
    for i, t in enumerate(BAD, 1):
        hits = check(t)
        flag = "✅揪出" if hits else "❌漏判"
        if not hits:
            ok = False
        cats = "、".join(sorted({h["类别"] for h in hits})) or "（无）"
        print(f"  {flag} #{i} [{cats}] {t}")

    print("\n[正例] 应全部通过（含滑档/退档解释不误伤）：")
    for i, t in enumerate(GOOD, 1):
        hits = check(t)
        flag = "✅通过" if not hits else "❌误伤"
        if hits:
            ok = False
            bad = "、".join(f"{h['类别']}:{h['命中']}" for h in hits)
            print(f"  {flag} #{i} 误判({bad}) ← {t}")
        else:
            print(f"  {flag} #{i} {t}")

    print("\n" + "=" * 60)
    print("✅ 评测通过：反例全揪出、正例无误伤。" if ok else "❌ 评测未过：见上方 ❌ 行。")
    print("=" * 60)
    return ok


if __name__ == "__main__":
    import sys
    sys.exit(0 if _run_eval() else 1)
